// ---------------------------------------------------------------------------
// Helpers I2C
// ---------------------------------------------------------------------------
static void ssd1306_send_cmd(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    i2c_write_blocking(SSD1306_I2C_PORT, SSD1306_I2C_ADDR, buf, 2, false);
}

static void ssd1306_send_data(uint8_t *data, size_t len) {
    uint8_t buf[len + 1];
    buf[0] = 0x40;
    memcpy(buf + 1, data, len);
    i2c_write_blocking(SSD1306_I2C_PORT, SSD1306_I2C_ADDR, buf, len + 1, false);
}

// ---------------------------------------------------------------------------
// Inicialização
// ---------------------------------------------------------------------------
void ssd1306_init(void) {
    i2c_init(SSD1306_I2C_PORT, 400 * 1000);
    gpio_set_function(SSD1306_SDA_PIN, GPIO_FUNC_I2C);
    gpio_set_function(SSD1306_SCL_PIN, GPIO_FUNC_I2C);
    gpio_pull_up(SSD1306_SDA_PIN);
    gpio_pull_up(SSD1306_SCL_PIN);

    sleep_ms(100);

    ssd1306_send_cmd(SSD1306_DISPLAY_OFF);
    ssd1306_send_cmd(SSD1306_SET_DISPLAY_CLK);   ssd1306_send_cmd(0x80);
    ssd1306_send_cmd(SSD1306_SET_MUX);            ssd1306_send_cmd(0x3F);
    ssd1306_send_cmd(SSD1306_SET_DISPLAY_OFFSET); ssd1306_send_cmd(0x00);
    ssd1306_send_cmd(SSD1306_SET_START_LINE | 0x00);
    ssd1306_send_cmd(SSD1306_CHARGE_PUMP);        ssd1306_send_cmd(0x14);
    ssd1306_send_cmd(SSD1306_MEMORY_MODE);        ssd1306_send_cmd(0x00);
    ssd1306_send_cmd(SSD1306_SEG_REMAP | 0x01);
    ssd1306_send_cmd(SSD1306_COM_SCAN_DEC);
    ssd1306_send_cmd(SSD1306_SET_COM_PINS);       ssd1306_send_cmd(0x12);
    ssd1306_send_cmd(SSD1306_SET_CONTRAST);       ssd1306_send_cmd(0xCF);
    ssd1306_send_cmd(SSD1306_SET_PRECHARGE);      ssd1306_send_cmd(0xF1);
    ssd1306_send_cmd(SSD1306_SET_VCOM_DETECT);    ssd1306_send_cmd(0x40);
    ssd1306_send_cmd(SSD1306_DISPLAY_ALL_ON_RES);
    ssd1306_send_cmd(SSD1306_NORMAL_DISPLAY);
    ssd1306_send_cmd(SSD1306_DISPLAY_ON);

    ssd1306_clear();
    ssd1306_show();
}

// ---------------------------------------------------------------------------
// Framebuffer
// ---------------------------------------------------------------------------
void ssd1306_clear(void) {
    memset(framebuf, 0, sizeof(framebuf));
}

void ssd1306_show(void) {
    ssd1306_send_cmd(SSD1306_COLUMN_ADDR); ssd1306_send_cmd(0); ssd1306_send_cmd(127);
    ssd1306_send_cmd(SSD1306_PAGE_ADDR);   ssd1306_send_cmd(0); ssd1306_send_cmd(7);
    ssd1306_send_data(framebuf, sizeof(framebuf));
}

void ssd1306_draw_pixel(int x, int y, bool on) {
    if (x < 0 || x >= SSD1306_WIDTH || y < 0 || y >= SSD1306_HEIGHT) return;
    if (on)
        framebuf[x + (y / 8) * SSD1306_WIDTH] |=  (1 << (y % 8));
    else
        framebuf[x + (y / 8) * SSD1306_WIDTH] &= ~(1 << (y % 8));
}

// ---------------------------------------------------------------------------
// Primitivas de desenho
// ---------------------------------------------------------------------------
void ssd1306_draw_char(int x, int y, char c, int scale) {
    if (c < 32 || c > 126) c = '?';
    const uint8_t *glyph = font5x7[c - 32];
    for (int col = 0; col < 5; col++) {
        uint8_t col_data = glyph[col];
        for (int row = 0; row < 7; row++) {
            bool on = (col_data >> row) & 1;
            for (int sx = 0; sx < scale; sx++)
                for (int sy = 0; sy < scale; sy++)
                    ssd1306_draw_pixel(x + col * scale + sx, y + row * scale + sy, on);
        }
    }
}

void ssd1306_draw_string(int x, int y, const char *str, int scale) {
    int cx = x;
    while (*str) {
        ssd1306_draw_char(cx, y, *str++, scale);
        cx += (5 + 1) * scale;
    }
}

void ssd1306_draw_rect(int x, int y, int w, int h, bool filled) {
    for (int i = x; i < x + w; i++)
        for (int j = y; j < y + h; j++)
            if (filled || i == x || i == x+w-1 || j == y || j == y+h-1)
                ssd1306_draw_pixel(i, j, true);
}

void ssd1306_draw_line(int x0, int y0, int x1, int y1) {
    int dx = abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
    int dy = -abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
    int err = dx + dy;
    while (true) {
        ssd1306_draw_pixel(x0, y0, true);
        if (x0 == x1 && y0 == y1) break;
        int e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}